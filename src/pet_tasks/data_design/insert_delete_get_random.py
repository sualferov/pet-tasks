# Цель: спроектировать структуру данных, которая выполняет вставку (insert), удаление (remove) и получение случайного
# элемента (getRandom) за строгое константное время O(1).
#
# Давайте проанализируем наши стандартные инструменты:
#     Хэш-сет (set) или Хэш-мапа (dict):
#     Обеспечивают insert и remove за O(1). Но мы не можем получить случайный элемент за O(1),
#     так как элементы в хэш-таблицах распределены по бакетам не подрят,
#     и в Python нет индексации вроде my_set[random_index]. Перевод в список list(my_set) займет O(N).
#
#     Динамический массив (list): Позволяет получить случайный элемент за O(1) по индексу: random.choice(my_list).
#     Вставка в конец массива (append) тоже работает за амортизированное O(1). Но вот удаление произвольного
#     элемента занимает O(N), так как нужно сдвигать все элементы после него, чтобы не было «дыр».
#
# Мы объединяем динамический массив (список) и хэш-мапу (словарь).В массиве nums
# мы храним сами значения элементов. Это нужно для эффективного getRandom.В хэш-мапе indices мы храним пары
# {значение: его_текущий_индекс_в_массиве}. Это нужно для мгновенного поиска элемента при удалении.
#
# Магия удаления за O(1):
# Чтобы удалить элемент из середины массива без сдвига остальных элементов (что заняло бы O(N)),
# мы применим трюк:
#     Находим индекс удаляемого элемента через хэш-мапу.
#     Меняем удаляемый элемент местами с самым последним элементом массива.
#     Обновляем индекс этого «бывшего последнего» элемента в хэш-мапе.
#     Теперь наш удаляемый элемент оказался в самом конце массива. Мы просто делаем nums.pop(),
#     что в Python работает строго за O(1).

import random


class RandomizedSet:
    """Структура случайного элемент."""

    def __init__(self) -> None:
        self.nums: list[int] = []  # Динамический массив для хранения самих чисел
        self.indices: dict[int, int] = {}  # Хэш-мапа для хранения: {число: его_индекс_в_nums}

    def insert(self, val: int) -> bool:
        """Вставляет элемент."""
        # Если элемент уже есть, возвращаем False
        if val in self.indices:
            return False

        # Добавляем в конец массива (O(1))
        self.indices[val] = len(self.nums)
        self.nums.append(val)
        return True

    def remove(self, val: int) -> bool:
        """Удаляет элемент."""
        # Если элемента нет в структуре, возвращаем False
        if val not in self.indices:
            return False

        # Шаг 1: Находим индекс удаляемого элемента и значение последнего элемента массива
        idx_to_remove = self.indices[val]
        last_val = self.nums[-1]

        # Шаг 2: Переносим последний элемент на место удаляемого
        self.nums[idx_to_remove] = last_val
        self.indices[last_val] = idx_to_remove

        # Шаг 3: Теперь удаляемый элемент находится в конце массива.
        # Удаляем его из массива и из хэш-мапы за O(1)
        self.nums.pop()
        del self.indices[val]

        return True

    def get_random(self) -> int:
        """Возвращает случайный элемент."""
        # random.choice для списка в Python работает за честное O(1)
        return int(random.choice(self.nums))
