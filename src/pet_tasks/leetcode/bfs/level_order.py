from collections import deque

from pet_tasks.leetcode.entities.tree_node import TreeNode


def level_order(root: TreeNode | None) -> list[list[int]]:
    """Возвращает список уровней дерева."""
    """
    Почему collections.deque, а не обычный list?
        В обычном списке Python операция pop(0) (удаление из начала) занимает время O(N), так как все
        остальные элементы сдвигаются в памяти. У deque (двусвязная очередь) операция popleft() выполняется
        за стабильное O(1).
    Как работает изоляция уровней?
        Когда мы заходим в цикл while queue, в очереди лежат элементы только одного конкретного уровня.
        Переменная level_size = len(queue) жестко задает рамки внутреннего цикла for. Все новые элементы (потомки),
        которые мы добавляем через queue.append(), встают в конец очереди и будут обработаны только на следующей
        итерации while.

    Сложность алгоритма
    Time Complexity (Временная сложность): O(N), где N — общее количество узлов в дереве.
        Мы посещаем каждый узел ровно один раз и выполняем операции O(1) для извлечения и добавления.
    Space Complexity (Пространственная сложность): O(W), где W — максимальная ширина дерева (количество узлов
        на самом населенном уровне). В худшем случае (полное бинарное дерево) на последнем уровне находится
        около N2 узлов, поэтому память оценивается как O(N).
    """
    # Шаг 1: Edge case (крайний случай) — пустое дерево
    if not root:
        return []

    result = []
    # Инициализируем очередь корнем дерева
    queue = deque([root])

    # Шаг 2: Основной цикл BFS
    while queue:
        # Фиксируем количество узлов на ТЕКУЩЕМ уровне
        level_size = len(queue)
        current_level = []

        # Разбираем ровно level_size элементов
        for _ in range(level_size):
            node = queue.popleft()
            current_level.append(node.val)

            # Добавляем левого потомка, если он есть
            if node.left:
                queue.append(node.left)
            # Добавляем правого потомка, если он есть
            if node.right:
                queue.append(node.right)

        # Добавляем готовый уровень в общий результат
        result.append(current_level)

    return result
