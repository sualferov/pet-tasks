from collections import deque


def rotting_oranges(grid: list[list[int]]) -> int:
    """Определяет за сколько секунд сгниют все апельсины."""
    """
    Зачем нам мульти-источниковый BFS?
        Гниение распространяется посекундно во все 4 стороны от каждого уже гнилого апельсина.
        Если мы запустим обычный BFS для каждой гнилой клетки по отдельности, мы получим неоптимальное
        оверлапящееся время. Поместив все изначально гнилые клетки в одну очередь, мы симулируем процесс
        поминутно и параллельно.
    Условие остановки:
        Нам нужно считать только те минуты, когда гниль реально распространяется.
        Если в очереди еще есть элементы, но свежих апельсинов на поле не осталось, время увеличивать не нужно.

    Разбор сложностей
    Time Complexity (Временная сложность):
        O(MхN), где M — количество строк, а N — количество столбцов.
        Мы обходим матрицу один раз для поиска начальных состояний, а затем каждая клетка может попасть
        в очередь BFS не более одного раза.
    Space Complexity (Пространственная сложность): O(MxN).
        В худшем случае (например, когда все апельсины гнилые) в очередь изначально запишутся все клетки матрицы.
    """
    rows, cols = len(grid), len(grid[0])
    queue: deque[tuple[int, int]] = deque()
    fresh_oranges = 0
    minutes = 0

    # Шаг 1: Сканируем сетку.
    # Находим все очаги гниения и считаем свежие апельсины.
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] == 2:
                queue.append((r, c))  # Добавляем координаты гнилого
            elif grid[r][c] == 1:
                fresh_oranges += 1  # Считаем свежие

    # Если изначально свежих апельсинов нет, то и времени нужно 0 минут
    if fresh_oranges == 0:
        return 0

    # Направления для движения: вверх, вниз, влево, вправо
    directions = [(-1, 0), (1, 0), (0, -1), (0, 1)]

    # Шаг 2: Мульти-источниковый BFS
    while queue and fresh_oranges > 0:
        minutes += 1
        # Фиксируем размер очереди для обработки строго одной "минуты" (уровня BFS)
        level_size = len(queue)

        for _ in range(level_size):
            r, c = queue.popleft()

            for dr, dc in directions:
                row, col = r + dr, c + dc

                # Если сосед в границах сетки и он свежий — заражаем его
                if 0 <= row < rows and 0 <= col < cols and grid[row][col] == 1:
                    grid[row][col] = 2
                    fresh_oranges -= 1
                    queue.append((row, col))

    # Шаг 3: Если после BFS остались свежие апельсины, значит, до них нельзя добраться
    return minutes if fresh_oranges == 0 else -1
