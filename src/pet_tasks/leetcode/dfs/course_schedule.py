from collections import defaultdict, deque


def can_finish(num_courses: int, prerequisites: list[list[int]]) -> bool:
    """Определяет наличие цикличности в зависимостях по крусам."""
    """
    1. Проектирование графа и подсчет входящих степеней
        Сначала мы инициализируем список смежности для представления ориентированного графа и массив для хранения
        количества входящих ребер (in-degree) каждого узла. Входящая степень у узла a увеличивается,
        если у курса есть пререквизит b
    2. Инициализация очереди
        Мы находим все курсы, у которых нет никаких предварительных условий (in_degree == 0), и добавляем
        их в очередь обработки deque. Эти курсы можно «пройти» сразу
    3. Обход в ширину (BFS) и декремент зависимостей
        Пока очередь не пуста, мы извлекаем курс, инкрементируем счетчик обработанных курсов и уменьшаем
        (in_degree) для всех зависимых от него курсов. Если у какого-то зависимого курса входящая степень падает
        до 0, это значит, что все его пререквизиты выполнены, и мы отправляем его в очередь.
    4. Проверка на наличие циклов
        Если итоговое количество обработанных курсов равно общему числу курсов numCourses, значит,
        граф ацикличен (DAG), и мы возвращаем True. В противном случае в графе был цикл, который заблокировал
        обработку части узлов, возвращаем False

    Сложность алгоритма
    Time Complexity (Временная сложность): O(V + E), где V = numCourses (вершины), а E = len(prerequisites) (ребра).
    Мы обходим каждую вершину и каждое ребро ровно один раз.

    Space Complexity (Пространственная сложность): O(V + E) для хранения структуры графа в defaultdict
    и массиве степеней.
    """
    # Шаг 1: Инициализируем граф и массив входящих степеней
    graph = defaultdict(list)
    in_degree = [0] * num_courses

    # Шаг 2: Заполняем структуру графа и считаем зависимости
    for course, prereq in prerequisites:
        graph[prereq].append(course)
        in_degree[course] += 1

    # Шаг 3: Добавляем в очередь курсы без зависимостей (in_degree == 0)
    queue = deque([i for i in range(num_courses) if in_degree[i] == 0])
    processed_courses = 0

    # Шаг 4: Обход графа в ширину (BFS)
    while queue:
        current = queue.popleft()
        processed_courses += 1

        # Уменьшаем входящую степень у всех зависимых курсов
        for neighbor in graph[current]:
            in_degree[neighbor] -= 1
            # Если все пререквизиты выполнены, добавляем в очередь
            if in_degree[neighbor] == 0:
                queue.append(neighbor)

    # Шаг 5: Если обработали все курсы, цикла нет
    return processed_courses == num_courses
