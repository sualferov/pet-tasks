from collections import deque


def num_islands(grid: list[list[str]]) -> int:
    """Возвращает кол-во островов."""
    """
    Мы будем использовать подход In-place модификации (прямо в матрице заменять '1' на '0' после посещения),
    чтобы не тратить память на структуру visited.

    Когда мы работаем с обходом матриц, всегда держи в голове три кита:
    Валидация границ:
        Строка if r < 0 or c < 0 or r >= ROWS or c >= COLS — это стандартный защитный гард (Guard Clause),
        который пишется в начале любого обхода клетки, чтобы избежать IndexError.
    Избегание зацикливания:
        Нам обязательно нужно помнить, где мы уже были. В этой задаче мы элегантно топим сушу (grid[r][c] = '0').
        Если по условиям задачи оригинальную матрицу менять нельзя, мы бы использовали хэш-множество пар visited=set()
        или булеву матрицу аналогичного размера.
    Определения связности:
        Внимательно читай условие. Здесь клетки соединяются только по горизонтали и вертикали (4 направления).
        В других задачах (как в №1091) могут учитываться диагонали (8 направлений).

    Анализ сложности
    Time Complexity (Временная сложность): O(MxN), где M — количество строк, а N — количество столбцов.
        Мы обходим каждую клетку матрицы. В худшем случае (если вся матрица состоит из суши) DFS обойдет все
        клетки за один вызов, а последующие итерации цикла просто пропустят их за O(1).
    Space Complexity (Пространственная сложность): O(MxN) в худшем случае для стека вызовов рекурсии.
        Если вся матрица заполнена сушей, стек рекурсии может вырасти до размера всей таблицы.
    """
    if not grid or not grid[0]:
        return 0

    rows, cols = len(grid), len(grid[0])
    island_count = 0

    def dfs(r: int, c: int) -> None:
        # Базовый случай: если вышли за границы или клетка — вода ('0'), выходим
        if r < 0 or c < 0 or r >= rows or c >= cols or grid[r][c] == '0':
            return

        # Помечаем текущую клетку как посещенную (превращаем сушу в воду)
        grid[r][c] = '0'

        # Рекурсивно обходим все 4 соседние клетки
        dfs(r + 1, c)  # вниз
        dfs(r - 1, c)  # вверх
        dfs(r, c + 1)  # вправо
        dfs(r, c - 1)  # влево

    # Сканируем всю матрицу
    for r in range(rows):
        for c in range(cols):
            # Как только находим кусок суши, мы нашли новый остров
            if grid[r][c] == '1':
                island_count += 1
                # Запускаем DFS, чтобы "затопить" весь этот остров целиком
                dfs(r, c)

    return island_count


def num_islands_no_recursion(grid: list[list[str]]) -> int:
    """Возвращает кол-во островов."""
    if not grid or not grid[0]:
        return 0

    rows, cols = len(grid), len(grid[0])
    island_count = 0
    directions = [(1, 0), (-1, 0), (0, 1), (0, -1)]

    # Сканируем матрицу линейно
    for r in range(rows):
        for c in range(cols):
            # Если нашли сушу — это начало нового острова
            if grid[r][c] == '1':
                island_count += 1

                # Инициализируем очередь для BFS и сразу "топим" стартовую клетку
                queue = deque([(r, c)])
                grid[r][c] = '0'

                # Запускаем BFS обход текущего острова
                while queue:
                    curr_r, curr_c = queue.popleft()

                    # Проверяем все 4 направления вокруг текущей клетки
                    for dr, dc in directions:
                        row, col = curr_r + dr, curr_c + dc

                        # Если сосед находится в границах и это суша
                        if 0 <= row < rows and 0 <= col < cols and grid[row][col] == '1':
                            queue.append((row, col))
                            grid[row][col] = '0'  # Топим сразу при добавлении в очередь!

    return island_count
