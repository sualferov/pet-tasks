from operator import itemgetter


def remove_overlap_intervals(intervals: list[list[int]]) -> int:
    """Возвращает кол-во интервалов для удаления, чтобы интервалы не пересеуались."""
    """
    Алгоритм:
    Сортировка: Сортируем интервалы не по началу, а по их конечной точке (end).
    Линейный обход: Храним конец последнего успешно оставленного интервала (prev_end).
    Сравнение:
        - Если начало текущего интервала больше или равно prev_end, значит они не пересекаются.
        Мы обновляем prev_end = current_end.
        - Если начало текущего интервала меньше prev_end, они пересекаются.
        Так как мы отсортировали по концам, текущий интервал гарантированно заканчивается позже или так же,
        как предыдущий. Значит, его выгоднее удалить, чтобы сохранить больше свободного места в будущем.
        Увеличиваем счетчик удалений.

    Пример работы алгоритма для intervals = [[1,2],[2,3],[3,4],[1,3]]
    1. После сортировки по концу (по x[1]):
    Массив примет вид: [[1,2],[2,3],[3,4],[1,3]]
    (интервал `1,3` ушел в конец, так как его правая граница 3 больше остальных).
    2. Инициализация: prev_end = 2 (конец интервала ``), removals = 0.
    3. Итерация 1 (current = ):
        Проверяем: 2 < 2 (start < prev_end)? Нет.
        Обновляем: prev_end = 3.
    4. Итерация 2 (current = ):
        Проверяем: 3 < 3? Нет.
        Обновляем: prev_end = 4.
    5. Итерация 3 (current = ):
        Проверяем: 1 < 4? Да! Найдено пересечение.
        Счетчик: removals += 1 (равно 1). prev_end остается равен 4.
    Итог: алгоритм вернул 1 (нужно удалить только интервал ``).
    """
    if not intervals:
        return 0

    # Шаг 1: Сортируем интервалы строго по их КОНЦУ (x[1])
    intervals.sort(key=itemgetter(1))

    # Переменная для подсчета удаленных интервалов
    removals = 0
    # Конец первого интервала берем за точку отсчета
    prev_end = intervals[0][1]

    # Шаг 2: Проходим по всем остальным интервалам
    for start, end in intervals[1:]:
        # Если текущий интервал начинается ДО того, как закончился предыдущий
        if start < prev_end:
            # Найдено пересечение -> "удаляем" текущий интервал
            removals += 1
        else:
            # Пересечения нет -> обновляем границу окончания
            prev_end = end

    return removals
