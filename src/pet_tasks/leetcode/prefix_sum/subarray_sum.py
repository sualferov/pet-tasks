# Префиксная сумма — это сумма всех элементов от начала массива до текущего индекса.
#
# Если сумма элементов от начала до индекса j равна current_sum, а сумма элементов от начала до какого-то
# более раннего индекса i равна current_sum - k, то сумма подмассива между i и j строго равна k:
#
# Двигаясь по массиву, мы считаем текущую префиксную сумму (current_sum) и смотрим в хэш-мапу:
# «А встречалась ли нам ранее префиксная сумма, равная current_sum - k?
# Если да, то сколько раз?». Мы прибавляем это количество к нашему результату.
#
# Сложность: O(N) & O(N)


def subarray_sum(nums: list[int], k: int) -> int:
    """Возвращает сумму подмассива."""
    # Хэш-карта для хранения частоты префиксных сумм: {сумма: сколько_раз_встретилась}
    # Инициализируем базовый случай: сумма 0 встретилась 1 раз (до начала массива)
    prefix_sums = {0: 1}

    count = 0
    current_sum = 0

    for num in nums:
        # Обновляем текущую префиксную сумму
        current_sum += num

        # Нам нужна ранняя префиксная сумма, равная (current_sum - k)
        target_sum = current_sum - k

        # Если такая сумма уже встречалась, добавляем её частоту к результату
        if target_sum in prefix_sums:
            count += prefix_sums[target_sum]

        # Сохраняем/обновляем частоту текущей префиксной суммы в хэш-мапе
        prefix_sums[current_sum] = prefix_sums.get(current_sum, 0) + 1

    return count
