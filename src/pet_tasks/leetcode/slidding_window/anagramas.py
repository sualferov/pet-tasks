# Анаграмма — это строка, состоящая из того же набора символов и в том же количестве, но в другом порядке.
#
# Размер окна: Поскольку мы ищем анаграммы строки p внутри строки s, размер нашего скользящего окна
# всегда строго фиксирован и равен len(p).
#
# Сравнение частот: Чтобы не сортировать подстроки на каждом шаге (сортировка убила бы асимптотику
# до O(N x M log M)), мы будем хранить частоты символов строки p и текущего окна строки s.
#
# Оптимизация под Python: Поскольку в условии задачи сказано, что строки состоят только из строчных латинских букв,
# мы можем использовать массив из 26 элементов или хэш-мапу. В Python для лаконичности кода на доске
# идеально подходит collections.Counter или обычные словари, так как их сравнение (dict1 == dict2)
# при размере алфавита до 26 символов происходит за O(1).
#
# Сложность: O(N) & O(1)

from collections import Counter


def find_anagrams(s: str, p: str) -> list[int]:
    """Возвращает индексы анаграммы."""
    ns, np = len(s), len(p)

    # Если строка s короче, чем искомый паттерн p, анаграмм быть не может
    if ns < np:
        return []

    result = []

    # Считаем эталонные частоты для строки p
    p_count = Counter(p)
    # Инициализируем частоты для первого окна в строке s (длиной len(p))
    s_count = Counter(s[:np])

    # Проверяем самое первое окно
    if s_count == p_count:
        result.append(0)

    # Двигаем окно фиксированного размера по строке s
    # right — индекс символа, который ВХОДИТ в окно справа
    for right in range(np, ns):
        # Символ, который ВЫХОДИТ из окна слева
        left_char = s[right - np]
        # Символ, который входит в окно справа
        right_char = s[right]

        # Добавляем новый символ в частотный словарь окна
        s_count[right_char] += 1

        # Убираем ушедший символ из частотного словаря окна
        s_count[left_char] -= 1
        # Важный Senior-штрих: удаляем ключ, если счетчик стал равен 0,
        # чтобы сравнение словарей s_count == p_count работало корректно
        if s_count[left_char] == 0:
            del s_count[left_char]

        # Если после сдвига окна частоты совпали — мы нашли анаграмму
        if s_count == p_count:
            # Индекс начала текущего окна равен (right - np + 1)
            result.append(right - np + 1)

    return result
