# Стратегия решения (Два указателя + Хэш-мапа)
# Оптимальный подход работает за линейное время O(N).
#
# Мы заводим два указателя: left и right, которые формируют границы нашего «окна» (текущей подстроки без повторений).
#     Указатель right шагает вперед на каждой итерации, расширяя окно и «читая» новый символ.
#     Мы храним в хэш-таблице seen пары {символ: его_последний_индекс}.
#     Критический момент: Если символ у указателя right уже есть в хэш-таблице, и его сохраненный индекс находится
#         внутри текущего окна (то есть seen[char] >= left), это значит, что мы поймали дубликат.
#         Нам нужно мгновенно «захлопнуть» окно слева, передвинув left на позицию seen[char] + 1
#         (сразу за предыдущее вхождение этого символа).
#     На каждом шаге мы обновляем позицию символа в хэш-мапе и пересчитываем максимальную длину:
#         max_len = max(max_len, right - left + 1).
#
# Сложность: O(N) & O(min(N, Alfavit)


def longest_substring(s: str) -> int:
    """Возвращает наибольшую подстроку."""
    seen: dict[str, int] = {}
    max_len = 0
    left = 0

    for right, char in enumerate(s):
        # Если символ уже встречался И его индекс находится внутри текущего окна
        if char in seen and seen[char] >= left:
            # Перемещаем левую границу окна за предыдущее вхождение символа
            left = seen[char] + 1

        # Обновляем или добавляем индекс текущего символа
        seen[char] = right

        # Считаем длину текущего валидного окна и обновляем максимум
        max_len = max(max_len, right - left + 1)

    return max_len
