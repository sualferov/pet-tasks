# Пример: nums[i] + nums[j] + nums[k] == 0
#
# Чтобы избежать дубликатов без сортировки, нам понадобятся два уровня фильтрации:
#    dups (внешний сет): Хранит элементы a, которые мы уже зафиксировали как первые элементы тройки.
#    Если число дублируется на верхнем уровне, мы его просто пропускаем.
#
#    seen (внутренний сет): Для каждого нового a мы запускаем поиск пары b и c. Мы идем по элементам после a.
#    Текущий элемент считаем за b. Тогда потенциальный третий элемент должен быть равен c = -a - b.
#        Если c уже лежит в seen, мы нашли валидную тройку.
#        Чтобы не выдать дубликат тройки (например, [-1, 2, -1] и [-1, -1, 2]), мы сохраняем найденную тройку
#        в результат в нормализованном виде.
#
# Сложность: O(N^2) & O(N)


def three_sum(nums: list[int]) -> list[list[int]]:
    """Возвращает список индексов, у которых сумма 3-х элементов равна 0."""
    result_set = set()
    dups = set()

    for i, a in enumerate(nums):
        # Если этот элемент уже обрабатывался как первый в тройке,
        # пропускаем его во избежание дубликатов
        if a in dups:
            continue
        dups.add(a)

        # Внутренний сет для поиска пары к элементу 'a'
        seen = set()

        # Проходим по всем элементам, стоящим после 'a'
        for b in nums[i + 1:]:
            c = -a - b

            # Если дополнение c уже встречалось в текущем проходе
            if c in seen:
                # Сортируем только 3 элемента (это O(1)), чтобы гарантировать
                # уникальность кортежа в результирующем сете
                triple = tuple(sorted((a, b, c)))
                result_set.add(triple)

            # Запоминаем текущий элемент b
            seen.add(b)

    # Превращаем сет кортежей обратно в список списков
    return [list(triple) for triple in result_set]
